#pragma once
#include "Bullet.h"

class CScoreUI;
enum eCOIN_TYPE;
class CPlayCoin : public CBullet
{
public:
	/*create를 호출하면 CObjectManager에서 메모리를 받는다.
	받은 메모리는 메모리풀에 미리 생성되어있던 메모리이다.*/
	static CPlayCoin* create(
		eCOIN_TYPE coinType,		//coin 타입 (브론즈, 실버, 골드 등)
		float boundingRadius,		//coin 충돌 범위
		float distance,				//coin 초기 거리
		float angle,				//coin 초기 각도 
		float speed,				//coin 초기 속도
		bool isFly,					//fly coin 인지 ground coin 인지
		Vec2 createPosition = Vec2(0.0f, 0.0f));

	virtual void Execute(float delta = 0.f) override;
	virtual void CollisionWithPlayer();
	virtual void CollisionWithPlanet();

protected:
	virtual bool init() override;
	virtual bool initVariable() override;

private:
	CPlayCoin(
		eCOIN_TYPE coinType,
		float boundingRadius,
		float distance,				
		float angle,
		float speed,
		bool isFly,
		Vec2 createPosition );

	virtual ~CPlayCoin(){};

private:
	CScoreUI* m_pUIScore;
	Vec2 m_CreatePos;
	eCOIN_TYPE m_CoinType;	//coin 타입
	float m_fCoinValue;
	float m_fDistance;
};

